#include <iostream>
using namespace std;

int main()
{
	cout<<"This is the program with switch stmt with initializer \n";
	int number;
	switch( cin>>number; number)
	{
	case 1:
		cout<<"@ case 1\n";
		break;
	case 2:
		cout<<"@ case 2\n";
		break;
	case 3:
		cout<<"@ case 3\n";
		break;
	case 4:
		cout<<"@ case 4\n";
		break;
	case 5:
		cout<<"@ case 5\n";
		break;
	case 6:
		cout<<"@ case 6\n";
		break;
	case 7:
		cout<<"@ case 7\n";
		break;
	default:
		cout<<"@ default\n";
		break;
	}
}